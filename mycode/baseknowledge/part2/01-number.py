# 数字


## 加
print(123 + 222)

## 减
print(123 - 222)

## 乘
print(1.5 * 4)

## 除
print(200 / 6)

## 幂
print(2 ** 10)

## 计算数字位数
print(len(str(2 ** 1000000)))

## 命令行中直接执行会显示异常
print(3.1415 * 2)

# math 函数

import math

## π
print(math.pi)

## 开方
print(math.sqrt(99))

print(math.ceil(99.1))
print(math.floor(99.1))
print(math.degrees(99))

import random

print(random.random)
print(random.choice([1, 2, 3, 4, 5]))

